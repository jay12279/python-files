#3. program to reverse a list
# A list is known as collection of items like numbers, words or any other data which stored together in one variable
# lists are written inside the square brackets [] which contains different types of data

#code to reverse a list

numbers = [10, 1120, 350, 40, 520]

reversed_list = numbers[::-1] #this expression means it starts from the end to move until the beginning 

print("Reversed list:", reversed_list)
