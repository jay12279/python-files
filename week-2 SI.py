#PROGRAM ON SIMPLE INTREST
#firstly the formula for simple intrest is simple intrest (SI)=PxRxT/100 P-priciple amount, R-rate of intrest per year or per month depends, T- time
P= float(input("Enter principle intrest:"))
R= float(input("Rate of intrest(%):"))
T= float(input("Enter Time (in years):"))

SI=(P*R*T)/100
         
print("principle intrest:",P)
print("Rate of intrest:",R)
print("Time:",T)
print("Simple Intrest =",SI)
